'''
Loops :

	Loop is used for repeating something over and over until a particular condition is satisfied.



Types of Loops :

1) For Loop                    2) While Loop                  3) do-while Loop



1) For Loop :--

		The for loop is used in the case where we need to execute some part of the code
until the given condition is satisfied.  It is better to use for loop if the number of iteration
is known in advance.


2) While Loop:--

		The while loop is to be used in the scenario where we don't know the number of
iterations in advance. The block of statements is executed in the while loop until the condition
specified in the while loop is satisfied.


3) do-while Loop:--

		The do-while loop continues until a given condition satisfies. It is also called
post tested loop. It is used when it is necessary to execute  the loop at least once.


Control Statements:

	Control statements are used for control the Loops.

Types of Control Statements:

1) Break               2) Continue               3)Pass


1) Break:--

		The break statement is used to terminate the loop.

2) Continue:--

		The continue statement is used to execute the next iteration of the loop.

3) Pass:---

		The pass statement is for null operations. Nothing will be executed in this statement.
'''